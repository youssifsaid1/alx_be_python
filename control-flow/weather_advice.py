weather = input("What's the weather like today? (sunny/rainy/cold): ")

if weather == "sunny":
    print("Wear a t-shirt and sunglasses.")
elif weather == "rainy":
    print("Take an umbrella and a raincoat.")
elif weather == "cold":
    print("Wear a jacket.")
else:
    print("Not sure about the weather, be prepared for anything.")
